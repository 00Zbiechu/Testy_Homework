package pl.testy.zadanie.model;

public class PersonDTO {
	private Long id;
	private String firstName;
	private String lastName;
	private Integer age;

	public Long getId() {
		return id;
	}

	public String getFirstName() {
		return firstName;
	}

	public String getLastName() {
		return lastName;
	}

	public Integer getAge() {
		return age;
	}
}
